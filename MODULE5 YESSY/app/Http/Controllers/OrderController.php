    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Product $products)
    {
        $orders = new Order();
        $orders->products_id = $request->input('products_id');
        $orders->buyer_name = $request->input('buyer_name');
        $orders->buyer_contact = $request->input('buyer_contact');
        $orders->amount = $request->input('amount');
        $orders->save();
        $products->decrement('stock');
        return redirect()->route('history')->withSuccess('Order Shoes Success');
    }

